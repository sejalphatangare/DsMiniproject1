package com.company.model;

public class Pair {
    private int a;
    private int b;

    public Pair(int a, int b) {
        this.a = a;
        this.b = b;
    }



    public void setA(int a) {
        this.a = a;
    }
    public int getA() {
        return a;
    }

    public void setB(int b) {
        this.b = b;
    }
    public int getB() {
        return b;
    }

    public Pair(){

    }
}
